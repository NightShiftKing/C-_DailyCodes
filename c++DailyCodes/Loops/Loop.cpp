#include<iostream>
using namespace std; 
int main(){

    
    for(int i = 70; i <= 80; i++){
        cout<< i << endl;   
    }
    
    for(int j = 100; j <= 300; j += 20){
        cout<< j << endl; 
    }

    for(int k = 25; k >= -25; k -= 5){
        cout<< k << endl; 
    }

    for(int p = 10000; p > 8; p/=5){
        cout<< p << endl; 
    }

    for(int n = 1; n <=5; n++ ){
        cout<< 2*n-1 << endl;
    }

}