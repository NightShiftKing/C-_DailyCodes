#include<iostream>
using namespace std;

int main() {
    for (int i = 0; i <= 18; i++) {
        cout << i << endl;
    }

    for (int j = 25; j <= 45; j++) {
        cout << j << endl;
    }

    for (int k = 40; k <= 80; k += 4) {
        cout << k << endl;
    }

    for (int potato = 0; potato <= -6; potato -= 3) {
        cout << potato << endl;
    }
}